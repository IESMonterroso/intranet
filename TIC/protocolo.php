				<h3>1. Protocolo de uso</h3>
				<br>
				
				<h4>1.1. Reserva del carro de portátiles</h4>
				<p>La reserva de los <strong>ordenadores</strong> o <strong>aulas TIC</strong> se realizará a través de la Intranet o de la Conserjería del mismo.</p>
				
				<br>
				
				<h4>1.2. Solicitud de la llave</h4>
				<p>En <strong>Conserjería</strong> se debe pedir la llave correspondiente al carro que se ha reservado. En ningún caso se dará esta llave a un/a alumno/a. Junto con la llave de la dependencia donde se guarda el carro estará la llave que abre el propio carro.</p>
				
				<br>
				
				<h4>1.3. Llevando el carro a clase</h4>
				<p>El carro con los ordenadores puede ser llevado al aula por el profesor o los alumnos, <strong>pero en ningún caso solo por los alumnos</strong>. El profesor debe supervisar en todo momento el transporte del carro hasta el aula.</p>
				
				<br>
				
				<h4>1.4. Repartiendo los portátiles</h4>
				<p>El reparto de los portátiles se llevará acabo <strong>con la colaboración de los ayudantes TIC</strong>. El alumno que recibe el ordenador debe revisar el estado en el que se le entrega el equipo y dar parte de forma inmediata en caso de encontrar cualquier problema. En este caso, el profesor tomará nota para proceder a continuación a registrar la incidencia mediante el formulario habilitado en la Intranet. Durante el reparto se debe rellenar la hoja de asignación ordenador-alumnado. Esta hoja, debidamente cumplimentada, se entregará en Conserjería para su archivo. Los ordenadores llevan un número identificativo que facilitará esta labor.</p>
				
				<br>
				
				<h4>1.5. Encendiendo los portátiles</h4>
				<p>La conexión inalámbrica de los ordenadores provoca que el arranque pueda llegar a ser extremadamente lento. Es mejor que los alumnos vayan encendiendo el ordenador conforme lo vayan recibiendo y que el proceso de reparto se haga con tranquilidad. En principio todos, alumnado y profesorado, pueden acceder con nombre de usuario <strong>usuario</strong> y contraseña <strong>usuario</strong>. Sin embargo, el acceso recomendado es el acceso personalizado, tanto de alumnos como profesores, mediante el uso de nombres de usuario y contraseñas individuales.</p>
				
				<p>El profesor puede obtener los nombres de usuario y contraseña de sus alumnos mediante el enlace <strong>Perfiles alumnos</strong>, y los suyos mediante el enlace <strong>Perfiles profesores</strong>. Es muy recomendable que la primera vez que el profesor o el alumno entren en el sistema procedan a cambiar la contraseña abriendo el navegador y dirigiéndose a la página <strong>http://c0/gesuser/</strong>.</p>
				
				<p>Nota: Si el ordenador avisa de que no hay red se debe comprobar que el botón de la red inalámbrica está en la posición ON y que el LED correspondiente está encendido (de color naranja).</p>
				
				<br>
				
				<h4>1.6. Usando los portátiles</h4>
				<p>Durante la clase procuraremos que el uso de los portátiles sea cuidadoso y adecuado. Debemos hacer entender a los/las alumnos/as lo importante que es cuidar un material que usarán y compartirán varios años. No debemos dudar en tomar nota y sancionar a los alumnos responsables de un mal uso intencionado del equipo.</p>
				
				<br>
				
				<h4>1.7. Recogida de los equipos</h4>
				<p>Los ordenadores serán recogidos con la colaboración de los <strong>ayudantes TIC</strong> que revisarán su estado. En caso de que se contemplen desperfectos se registrará la incidencia en la Intranet. Se comprobará que están apagados y se enchufarán a su cargador en la bandeja correspondiente. Se comprobará que se han devuelto todos los portátiles, que la carpeta esté en la bandeja inferior de la izquierda y se cerrarán las puertas.</p>
				
				<br>
				
				<h4>1.8. Y para terminar</h4>
				<p>El carro lo debemos llevar al lugar donde se guarda y dejarlo enchufado para que la próxima vez que se use estén los portátiles cargados. Nunca dejaremos abierta la estancia donde se guarda el carro. Devolveremos las llaves y registraremos, si los hubiera, los partes de incidencias en la Intranet.</p>
				
				<hr>
				
				<h3>2. Recomendaciones</h3>
				
				<br>
				
				<p>2.1. Es recomendable que, antes de usar los portátiles en clase, comprobemos que la actividad que hemos programado funcione en los ordenadores de la red TIC. Esto se puede hacer cómodamente en el ordenador del Departamento (siempre que se conecte a la red TIC).</p>
				
				<br>
				
				<p>1.2. Una vez comprobado el funcionamiento debemos prever que, en caso de usar un recurso directamente desde internet, varias decenas de ordenadores trabajando a la vez en una página puede no permitir un trabajo fluido. Una clase con ordenadores que no funcionan puede ser muy frustrante y provocará que el alumnado no se concentre y se pierda la hora.</p>
				
				<br>
				
				<p>1.3. Si lo que queremos es mostrar a los alumnos un recurso no interactivo (una presentación. una página web, un vídeo etc..) es más fácil usar un portátil y un proyector en vez de repartir 15 ordenadores. Se pondrá a disposición del profesorado un sistema de reserva de proyectores en la Intranet.</p>
				
				<br>
				
				<p>1.4. A continuación se enumeran algunas circunstancias que pueden dañar gravemente el material:</p>
				
				<ol type="a">
					<li>Dejar caer sobre la mesa un portátil desde una altura de cinco centímetros puede dañar diversos componentes, entre ellos el disco duro.</li>
					<li>Abrir o cerrar bruscamente el portátil puede provocar daños en el mecanismo de la pantalla.</li>
					<li>Tocar la pantalla con los dedos o con cualquier objeto puede dejar marcas y arañazos permanentes.</li>
					<li>El teclado es de tacto suave y por tanto no es necesario aporrearlo. Un teclado de portátil no se puede cambiar como uno de sobremesa.</li>
					<li>La suciedad en general y los líquidos en particular son incompatibles con el ordenador portátil. No debemos dejar que se use con las manos sucias o mojadas y, por supuesto, no debemos tener cerca ningún tipo de líquido para evitar derramarlo por accidente sobre el equipo.</li>
				</ol>
				
