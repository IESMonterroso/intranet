      <h4>
        Prueba de Acceso a la Universidad (SELECTIVIDAD)
        </h4>
      <p>La normativa que regula el acceso a la Universidad es: <br>
        # Real Decreto 1892/2008, de 14 de noviembre, por el que se regulan 
        las condiciones para el acceso a la Universidad. (BOE 24-11-2008) <br />
        # Correcci&oacute;n de errores - Real Decreto 1892 (BOE 21-7-2009)<br />
        # Correcci&oacute;n de errores R.D. 1892 (BOE 28-3-2009)<br />
        # Par&aacute;metros de ponderaci&oacute;n P.A.U. 2010 <br />
        # Par&aacute;metros de ponderaci&oacute;n P.A.U. 2011 <br />
        # Par&aacute;metros de ponderaci&oacute;n A.U. (Alumnado Ciclos Formativos) 
        <br />
        # Adscripci&oacute;n Bachillerato-Universidad <br />
        # Orden EDU 268/2010 - Para evitar discriminaci&oacute;n del alumnado 
        por cambios de adscripci&oacute;n (v&iacute;as)</p>
      <p>La prueba de acceso tiene por finalidad valorar, con car&aacute;cter 
        objetivo, la madurez acad&eacute;mica del estudiante, as&iacute; como 
        los conocimientos y capacidades adquiridos en el Bachillerato y su capacidad 
        para seguir con &eacute;xito las ense&ntilde;anzas universitarias oficiales 
        de Grado. <br>
        La valoración se expresará con una calificación numérica con dos cifras 
        decimales, que permitirá la ordenación de las solicitudes de admisión 
        para la adjudicación de plazas en la Universidad. <br>
        La prueba se realizar&aacute; en la Universidad a la que est&aacute; adscrito 
        el Instituto o Colegio en el que se hubiera obtenido el T&iacute;tulo 
        de Bachillerato. </p>
      <p>La P.A.U. se estructura en dos fases:</p>
      <p><strong>Fase general, </strong>que constar&aacute; de cuatro ejercicios: 
        </p>
      <ul>
        <li>Comentario de un texto en Lengua Castellana (habrá dos opciones para 
          elegir una)</li>
        <li>Ejercicio sobre Historia de la Filosof&iacute;a o sobre Historia de 
          Espa&ntilde;a. El alumno elegir&aacute; en la solicitud de inscripci&oacute;n 
          en la prueba la materia de examen. (Cada ejercicio tendr&aacute; dos 
          opciones para elegir una).</li>
        <li>Ejercicio sobre Lengua Extranjera. Este ejercicio tendr&aacute; una 
          parte escrita (con dos opciones y otra oral. La prueba oral entrar&aacute; 
          en vigor en el a&ntilde;o 2012.</li>
        <li>Ejercicio (con dos opciones para elegir una) sobre una de las materias 
          de la modalidad de 2&ordm; de Bachillerato cursada por el alumno y que 
          &eacute;ste elegir&aacute; al inscribirse en la prueba.</li>
        </ul>
      <p>Cada ejercicio tendr&aacute; una duraci&oacute;n m&aacute;xima de hora 
        y media con un intervalo m&iacute;nimo de 45 minutos entre el final de 
        un ejercicio y el inicio del siguiente.</p>
      <p>Cada ejercicio se calificar&aacute; de 0 a 10 puntos, con dos cifras 
        decimales, y la calificaci&oacute;n de la fase general ser&aacute; la 
        media aritm&eacute;tica de las calificaciones de los cuatro ejercicios. 
        </p>
      <p><strong>Fase espec&iacute;fica</strong>:</p>
      <p>Cada estudiante podr&aacute; examinarse de cualquiera de las materias 
        de modalidad de segundo de bachillerato, aunque no las haya cursado, distintas 
        de la escogida en la fase general. Se considerar&aacute; que la materia 
        ha sido superada si obtiene una nota mayor o igual a 5.</p>
      <p><strong>Superaci&oacute;n de la P.A.U</strong>.</p>
      <p>Un estudiante habr&aacute; superado la Selectividad si obtiene una nota 
        mayor o igual que 5 al realizar la media ponderada del 60% de la nota 
        media de bachillerato y el 40% de la calificaci&oacute;n de la fase general, 
        y siempre que haya obtenido en dicha fase general una nota m&iacute;nima 
        de 4. </p>
      <p>Para la adjudicaci&oacute;n de plazas en la Universidad se tendr&aacute; 
        en cuenta como nota de admisi&oacute;n la siguiente:</p>
      <p>Nota de Admisi&oacute;n=0,6*Nota Media Bachillerato+0,4*Calificaci&oacute;n 
        Fase General+a*M1+b*M2 siendo:</p>
      <ul>
        <li>a, b= Par&aacute;metros de ponderaci&oacute;n de las materias espec&iacute;ficas 
          que valdr&aacute;n 0,1 o que determinar&aacute;n las Universidades en 
          funci&oacute;n de las titulaciones y que podr&aacute;n elevarse hasta 
          0,2.</li>  <li>M1 y M2=calificaciones de las materias superadas en la fase espec&iacute;fica 
            que maximicen la Nota de Admisi&oacute;n. </li>
        </ul>
