      <h4></a>Modalidades, Programas y Asignaturas.</h4>
      <p>En el Bachillerato L.O.E. se presentan tres modalidades: 
        Ciencias y Tecnolog&iacute;a, Humanidades y Ciencias Sociales y Arte. 
        Nuestro Centro oferta las dos primeras.</p>
      <p><br />
        Las materias que se estudian en estas modalidades pueden ser de tres 
        tipos:<br />
        </p>
      <ol>
        <li> Comunes: para todos los alumnos con independencia de la modalidad 
          elegida. </li>
        <li> Materias propias de la modalidad: son espec&iacute;ficas para cada 
          una de las modalidades. Deber&aacute;n cursarlas todos los alumnos 
          que hayan elegido esa modalidad. El alumno elegir&aacute; tres de 
          estas materias en cada uno de los cursos. </li>
        <li> Materias optativas: son materias que otorgan una mayor flexibilidad 
          al Bachillerato, aumentando la participaci&oacute;n del alumno en 
          la definici&oacute;n de su propio curr&iacute;culo. Una materia optativa 
          ser&aacute; siempre la Segunda Lengua Extranjera. . </li>
        </ol>
