      <p>Los estudios de Bachillerato constituyen una etapa, a continuaci&oacute;n 
        de la E.S.O., que tiene caracter voluntario frente al obligatoro de 
        la E.S.O., y cuya duraci&oacute;n es de dos cursos acad&eacute;micos.</p>
      <p>La normativa que regula el Bachillerato es la Ley Org&aacute;nica 
        2/2006, de 3 de mayo, (L.O.E.), y los Decretos y &Oacute;rdenes que 
        la desarrollan: 
        <br>
        - Real Decreto 1467/2007, de 2 de noviembre, por el que se establece 
        la estructura del bachillerato y se fijan sus enseñanzas mínimas. (BOE 
        6/11/2007)<BR>
        - Decreto 416/2008, de 22 de julio, por el que se establece la ordenación 
        y las enseñanzas correspondientes al Bachillerato en Andalucía. (BOJA 
        28/7/2008).<br>
        - Orden de 5/8/2008, por el que se desarrolla el currículo correspondiente 
        al Bachillerato en Andalucía. (BOJA 26-8-2008)<br>
        - Orden de 15-12-2008, por la que se establece la ordenación de la evaluación 
        del proceso de aprendizaje del alumnado de bachillerato en la Comunidad 
        Autónoma de Andalucía (BOJA de 5-1-2009).<br>
        - Instrucciones de 16-6-2009, de la Dirección General de Ordenación 
        y Evaluación Educativa, sobre la permanencia en el primer curso de bachillerato 
        del alumnado con tres o cuatro materias no superadas. </p>
      <p>El I.E.S. Monterroso ofrece dos modalidades de Bachillerato: 
        <ul>
          <li> Ciencias y Tecnolog&iacute;a . </li>
          <li> Humanidades y Ciencias Sociales. </li>
        </ul>
      <hr />
      <h4>Condiciones requeridas para cursar Bachillerato. 
        </h4>
      <p>Podr&aacute;n acceder al Bachillerato aquellos alumnos que 
        se encuentren en alguna de las siguientes situaciones:</p>
      <ul>
        <li> Estar en posesi&oacute;n del t&iacute;tulo de Graduado en Educaci&oacute;n 
          Secundaria; </li>
        <li> Haber superado los estudios del primer Ciclo de la Reforma; </li>
        <li> Haber obtenido el t&iacute;tulo de T&eacute;cnico Auxiliar de la 
          F.P.I.; </li>
        <li> Haber aprobado el 2&ordm; curso de B.U.P.; </li>
        </ul>
      <hr />
      <h4>Titulaci&oacute;n y Salidas .</h4>
      <p>Al terminar los estudios de Bachillerato, el alumno consigue 
        el Titulo de Bachillerato. Este t&iacute;tulo le abre las siguientes 
        posibilidades:</p>
      <ul>
        <li> Los Ciclos Formativos de Grado Superior.</li>
        <li> Estudios Universitarios en las distintas Escuelas y Facultades 
          del Estado (una vez superadas las P.A.U.)<br />
          </li>
        </ul>
