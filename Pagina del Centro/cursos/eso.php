          <p>El I.E.S. Monterroso, que inicialmente imparti&oacute; s&oacute;lo 
            3&ordm; y 4&ordm; de ESO, ofrece desde el curso 2004/2005 toda la Ense&ntilde;anza 
            Secundaria Obligatoria: 1&ordm;, 2&ordm;, 3&ordm; y 4&ordm; de ESO.</p>
          <p> La normativa que regula la E.S.O. desde el curso 2007/2008 es la L.O.E. 
            (Ley Org&aacute;nica de la Educaci&oacute;n) y los Decretos y &Oacute;rdenes 
            que la desarrollan, que en Andaluc&iacute;a son:<br>
            - Decreto 231/2007 (BOJA nº 156 de 8 de agosto de 2007)<br>
            - Orden de 10 de Agosto de 2007 que desarrolla el currículo(BOJA nº 
            171 de 30 de agosto de 2007)<br>
            - Orden de 10 de Agosto de 2007 que regula la evaluación (BOJA nº 166 
            de 23 de agosto de 2007) <br>
            - Instrucciones de 17-12-2007, de la Dirección General de Ordenación 
            y Evaluación Educativa, por la que se complementa la normativa sobre 
            evaluación del proceso de aprendizaje del alumnado de Educación Secundaria 
            Obligatoria. </p>
          <p>La finalidad de esta etapa consiste en lograr que el alumnado 
            adquiera los elementos b&aacute;sicos de la cultura, especialmente en 
            sus aspectos human&iacute;stico, art&iacute;stico, cient&iacute;fico 
            y tecnol&oacute;gico; desarrollar y consolidar en &eacute;l h&aacute;bitos 
            de estudio y de trabajo; prepararle para su incorporaci&oacute;n a estudios 
            superiores y para su inserci&oacute;n laboral, y formarle para el ejercicio 
            de sus derechos y obligaciones en la vida como ciudadano o ciudadana.</p>

            <hr /><h5>Normas generales de ordenaci&oacute;n de la etapa</h5>
          <p>La etapa de educaci&oacute;n secundaria obligatoria tiene 
            car&aacute;cter obligatorio y gratuito y constituye, junto con la educaci&oacute;n 
            primaria, la educaci&oacute;n b&aacute;sica. Comprende cuatro cursos 
            acad&eacute;micos, que se seguir&aacute;n ordinariamente entre los doce 
            y los diecis&eacute;is a&ntilde;os de edad. Con car&aacute;cter general, 
            el alumnado tendr&aacute; derecho a permanecer escolarizado en r&eacute;gimen 
            ordinario hasta los dieciocho a&ntilde;os de edad cumplidos en el a&ntilde;o 
            que finalice el curso. <br />
            La educaci&oacute;n secundaria obligatoria se organiza en diferentes 
            materias. El cuarto curso tendr&aacute; car&aacute;cter orientador, 
            tanto para los estudios post-obligatorios como para la incorporaci&oacute;n 
            a la vida laboral. <br />
            La educaci&oacute;n secundaria obligatoria se organizar&aacute; de acuerdo 
            a los principios de educaci&oacute;n com&uacute;n y de atenci&oacute;n 
            a a la diversidad del alumnado. <br />
            A tales efectos, se pondr&aacute; especial &eacute;nfasis en la adquisici&oacute;n 
            de las competencias b&aacute;sicas, en la detecci&oacute;n y tratamiento 
            de las dificultades de aprendizaje tan pronto como se produzcan, en 
            la tutor&iacute;a y orientaci&oacute;n educativa del alumnado y en la 
            relaci&oacute;n con las familias para apoyar el proceso educativo de 
            sus hijos e hijas. <br />
            La educaci&oacute;n secundaria obligatoria se coordinar&aacute; con 
            la educaci&oacute;n primaria y con las etapas posteriores del sistema 
            educativo, con objeto de garantizar una adecuada transici&oacute;n del 
            alumnado entre ellas y facilitar la continuidad de su proceso educativo. 
            <br />
            La acci&oacute;n educativa en la educaci&oacute;n secundaria obligatoria 
            procurar&aacute; la integraci&oacute;n de las distintas experiencias 
            y aprendizajes del alumnado y se adaptar&aacute; a sus ritmos de trabajo. 
            <br />
            La acci&oacute;n educativa en la educaci&oacute;n secundaria obligatoria 
            prestar&aacute; especial atenci&oacute;n a aquellas &aacute;reas de 
            conocimiento que poseen un car&aacute;cter instrumental. <br />
          </p>
            <hr /><h5>Evaluaci&oacute;n.</h5>
          <p>La evaluaci&oacute;n del proceso de aprendizaje del alumnado ser&aacute; 
            continua y diferenciada seg&uacute;n las distintas materias del curr&iacute;culo. 
            <br />
            Dicha evaluaci&oacute;n se llevar&aacute; a cabo por el profesorado, 
            teniendo en cuenta los diferentes elementos del curr&iacute;culo, preferentemente 
            a trav&eacute;s de la observaci&oacute;n continuada de la evoluci&oacute;n 
            del proceso de aprendizaje de cada alumno o alumna y de su maduraci&oacute;n 
            personal, sin perjuicio de las pruebas que, en su caso, realice el alumnado. 
            En todo caso, los criterios de evaluaci&oacute;n de las materias ser&aacute;n 
            referente fundamental para valorar tanto el grado de adquisici&oacute;n 
            de las competencias b&aacute;sicas como el de consecuci&oacute;n de 
            los objetivos. <br />
            El equipo docente, coordinado por quien ejerza la tutor&iacute;a, actuar&aacute; 
            de manera colegiada a lo largo del proceso de evaluaci&oacute;n y en 
            la adopci&oacute;n de las decisiones resultantes del mismo. <br />
            En el proceso de evaluaci&oacute;n continua, cuando el progreso de un 
            alumno o alumna no sea el adecuado, se establecer&aacute;n medidas de 
            refuerzo educativo. Estas medidas se adoptar&aacute;n en cualquier momento 
            del curso, tan pronto como se detecten las dificultades y estar&aacute;n 
            dirigidas a garantizar la adquisici&oacute;n de los aprendizajes imprescindibles 
            para continuar el proceso educativo. <br />
            Asimismo, la Consejer&iacute;a competente en materia de educaci&oacute;n 
            establecer&aacute; por Orden la obligaci&oacute;n del profesorado de 
            evaluar tanto los aprendizajes del alumnado como los procesos de ense&ntilde;anza 
            y su propia pr&aacute;ctica docente. </p>
            <hr /><h5>Promoci&oacute;n del alumnado.</h5>
          <p>Al finalizar cada uno de los cursos de la etapa y como consecuencia 
            del proceso de evaluaci&oacute;n, el equipo docente tomar&aacute; las 
            decisiones correspondientes sobre la promoci&oacute;n del alumnado al 
            curso siguiente. <br />
            Las decisiones sobre la promoci&oacute;n del alumnado de un curso a 
            otro dentro de la etapa ser&aacute;n adoptadas de forma colegiada por 
            el equipo docente, con el asesoramiento del departamento de orientaci&oacute;n, 
            atendiendo a la adquisici&oacute;n de las competencias b&aacute;sicas 
            y a la consecuci&oacute;n de los objetivos. Los centros establecer&aacute;n 
            en sus proyectos educativos la forma en que el alumno o la alumna y 
            su padre, madre o tutores legales puedan ser o&iacute;dos. <br />
            Se promocionar&aacute; al curso siguiente cuando se hayan superado los 
            objetivos de las materias cursadas o se tenga evaluaci&oacute;n negativa 
            en dos materias como m&aacute;ximo, y se repetir&aacute; curso con evaluaci&oacute;n 
            negativa en tres o m&aacute;s materias. Excepcionalmente, podr&aacute; 
            autorizarse la promoci&oacute;n con evaluaci&oacute;n negativa en tres 
            materias cuando el equipo docente considere que la naturaleza de sus 
            dificultades no le impide seguir con &eacute;xito el curso siguiente, 
            que tiene expectativas favorables de recuperaci&oacute;n, a partir de 
            las competencias b&aacute;sicas alcanzadas, y que dicha promoci&oacute;n 
            beneficiar&aacute; su evoluci&oacute;n acad&eacute;mica. <br />
            Quien promocione sin haber superado todas las materias seguir&aacute; 
            un programa de refuerzo destinado a la recuperaci&oacute;n de los aprendizajes 
            no adquiridos y deber&aacute; superar la evaluaci&oacute;n correspondiente 
            a dicho programa. Esta circunstancia ser&aacute; tenida en cuenta a 
            los efectos de calificaci&oacute;n de las materias no superadas, as&iacute; 
            como a los de promoci&oacute;n y, en su caso, obtenci&oacute;n de la 
            titulaci&oacute;n. </p>
          <p>Corresponde a los departamentos did&aacute;cticos la organizaci&oacute;n 
            de estos programas de refuerzo. De su contenido se informar&aacute; 
            al alumnado y a sus padres, madres o tutores al comienzo del curso escolar. 
            <br />
            Quien no promocione deber&aacute; permanecer un a&ntilde;o m&aacute;s 
            en el mismo curso. Esta medida deber&aacute; ir acompa&ntilde;ada de 
            un plan espec&iacute;fico personalizado, orientado a la superaci&oacute;n 
            de las dificultades detectadas en el curso anterior. Los centros docentes 
            organizar&aacute;n este plan de acuerdo con lo que, a tales efectos, 
            establezca por Orden la Consejer&iacute;a competente en materia de educaci&oacute;n. 
            <br />
            El alumnado podr&aacute; repetir el mismo curso una sola vez y dos veces 
            como m&aacute;ximo dentro de la etapa. Excepcionalmente podr&aacute; 
            repetir una segunda vez en cuarto curso si no ha repetido en cursos 
            anteriores de la etapa. Cuando la segunda repetici&oacute;n deba producirse 
            en el &uacute;ltimo curso de la etapa, se prolongar&aacute; un a&ntilde;o 
            el l&iacute;mite de edad establecido. <br />
            Con el fin de facilitar al alumnado la recuperaci&oacute;n de las materias 
            con evaluaci&oacute;n negativa, por Orden de la Consejer&iacute;a competente 
            en materia de educaci&oacute;n se determinar&aacute;n las condiciones 
            y se regular&aacute; el procedimiento para que los centros docentes 
            organicen en el mes de septiembre las oportunas pruebas extraordinarias 
            en cada uno de los cursos. <br />
          </p>
            <hr /><h5>Titulaci&oacute;n.</h5>
		    <p>
		      El alumnado que supere todas las materias de la etapa obtendr&aacute; 
		      el t&iacute;tulo de Graduado en Educaci&oacute;n Secundaria Obligatoria. 
		      Asimismo, podr&aacute;n obtener dicho t&iacute;tulo aquellos alumnos 
		      o alumnas que hayan finalizado el curso con evaluaci&oacute;n negativa 
		      en una o dos materias, y excepcionalmente en tres, siempre que el 
		      equipo docente considere que la naturaleza y el peso de las mismas 
		      en el conjunto de la etapa no les ha impedido alcanzar las competencias 
		      b&aacute;sicas y los objetivos de la etapa. <br />
		      La decisi&oacute;n sobre la obtenci&oacute;n de la titulaci&oacute;n 
		      ser&aacute; adoptada de forma colegiada por el equipo docente, con 
		      el asesoramiento del departamento de orientaci&oacute;n. Los centros 
		      establecer&aacute;n en sus proyectos educativos la forma en que el 
		      alumno o la alumna y su padre, madre o tutores legales puedan ser 
		      o&iacute;dos. <br />
		      El alumnado que curse programas de diversificaci&oacute;n curricular 
		      obtendr&aacute; el t&iacute;tulo de Graduado en Educaci&oacute;n Secundaria 
		      Obligatoria si supera todos los &aacute;mbitos y materias que integran 
		      el programa. Asimismo, podr&aacute;n obtener dicho t&iacute;tulo aquellos 
		      alumnos y alumnas que, habiendo superado los dos &aacute;mbitos, tengan 
		      evaluaci&oacute;n negativa en una o dos materias, y excepcionalmente 
		      en tres, siempre que a juicio del equipo docente hayan alcanzado las 
		      competencias b&aacute;sicas y los objetivos de la etapa. La nota media 
		      del expediente de ESO correspondiente al alumnado de un programa de 
		      diversificaci&oacute;n curricular se calcular&aacute; teniendo en 
		      cuenta &uacute;nicamente las materias de dicho programa.<br />
		      El alumnado que al finalizar la etapa no haya obtenido el t&iacute;tulo 
		      de Graduado en Educaci&oacute;n Secundaria Obligatoria y tenga la 
		      edad m&aacute;xima de escolarizaci&oacute;n dispondr&aacute;, durante 
		      los dos a&ntilde;os siguientes, de una convocatoria anual de pruebas 
		      para superar aquellas materias pendientes de calificaci&oacute;n positiva, 
		      de conformidad con lo que, a tales efectos, establezca por Orden la 
		      Consejer&iacute;a competente en materia de educaci&oacute;n, siempre 
		      que el n&uacute;mero de &eacute;stas no sea superior a cinco. <br />
		      El alumnado que curse la educaci&oacute;n secundaria obligatoria y 
		      no obtenga el t&iacute;tulo recibir&aacute; un certificado de escolaridad 
		      en el que consten los a&ntilde;os y materias cursados. <br />
	        </p>
 
