      <p>Los programas de cualificaci&oacute;n profesional inicial 
        se establecen como una medida de atenci&oacute;n a la diversidad que 
        debe contribuir a evitar el abandono escolar previo a la finalizaci&oacute;n 
        de la educaci&oacute;n secundaria obligatoria, abriendo nuevas expectativas 
        de formaci&oacute;n y dando acceso a una vida laboral cualificada a 
        aquellos j&oacute;venes desescolarizados que se encuentran en situaci&oacute;n 
        de desventaja sociolaboral y educativa, manteniendo abierta, al mismo 
        tiempo, la posibilidad de obtenci&oacute;n de la titulaci&oacute;n b&aacute;sica 
        para todos ellos.</p>
      
      <p> Nuestro Instituto ofrece desde el curso 2008/2009 el P.C.P.I. 
        Auxiliar Informático. </p>
      <p>La normativa que regula este P.C.P.I. es la Ley Org&aacute;nica 
        2/2006, de 3 de mayo, (L.O.E.), y la Orden y Resoluci&oacute;n que la 
        desarrollan en Andaluc&iacute;a: <br>
        - Orden de 24 de junio de 2008, por la que se regulan los programas 
        de cualificaci&oacute;n profesional inicial (BOJA de 7-8-2008).<BR>
        - Resoluci&oacute;n de 18 de agosto de 2008, de la Direcci&oacute;n 
        General de Formaci&oacute;n Profesional y Educaci&oacute;n Permanente, 
        por la que se establece el perfil profesional de Auxiliar Inform&aacute;tico 
        y el curr&iacute;culo de los m&oacute;dulos espec&iacute;ficos del Programa 
        de Cualificaci&oacute;n Profesional Inicial correspondiente. (BOJA de 
        2-9-2008)</p>
      <p>Los objetivos de &eacute;ste y de todos los programas de 
        cualificaci&oacute;n profesional inicial son los siguientes:
  <ul>
    <li>Ampliar las competencias b&aacute;sicas del alumnado para proseguir 
      estudios en las diferentes ense&ntilde;anzas. </li>
    <li> Permitir que el alumnado alcance las competencias profesionales 
      propias de una cualificaci&oacute;n de nivel uno de la estructura 
      actual del Cat&aacute;logo Nacional de Cualificaciones Profesionales 
      creado por la Ley Org&aacute;nica 5/2002, de 19 de junio, de las Cualificaciones 
      y de la Formaci&oacute;n Profesional. </li>
    <li> Dotar al alumnado de posibilidades reales para una inserci&oacute;n 
      sociolaboral satisfactoria.</li>
    </ul>
      
      <hr /><h4>Requisitos de acceso a un P.C.P.I. </h4>
      <p>Podr&aacute;n incorporarse a los programas de cualificaci&oacute;n 
        profesional inicial las personas que se encuentren en alguno de
        los siguientes casos:</p>
      
      <ul>
        <li> Jóvenes menores de 21 años que cumplan al menos 16 en el año natural 
          de comienzo del programa y no hayan obtenido el título de Graduado en 
          Educación Secundaria Obligatoria. </li>
        <li> J&oacute;venes menores de 22 a&ntilde;os que cumplan al menos 16 
          en el a&ntilde;o natural de comienzo del programa y est&eacute;n diagnosticados 
          como de necesidades educativas especiales debidas a diferentes grados 
          y tipos de capacidades personales de orden f&iacute;sico, ps&iacute;quico, 
          cognitivo o sensorial y no hayan obtenido el t&iacute;tulo de Graduado 
          en Educaci&oacute;n Secundaria Obligatoria; </li>
        <li> Excepcionalmente y con el acuerdo del alumno o alumna y sus padres 
          o tutores legales, podr&aacute;n incorporarse j&oacute;venes de 15 a&ntilde;os 
          de edad cumplidos en el a&ntilde;o natural de iniciaci&oacute;n del 
          programa que, habiendo realizado el segundo curso de la educaci&oacute;n 
          secundaria obligatoria, no est&eacute;n en condiciones de promocionar 
          al tercer curso y hayan repetido ya una vez en esta etapa. En todo caso 
          su incorporaci&oacute;n requerir&aacute; la evaluaci&oacute;n acad&eacute;mica 
          del equipo docente, la evaluaci&oacute;n psicopedag&oacute;gica del 
          orientador u orientadora del centro y el compromiso del alumno o alumna 
          a cursar los m&oacute;dulos voluntarios del programa. </li>
        </ul>
      <hr /><h4>Estructura del P.C.P.I. Auxiliar Inform&aacute;tico</h4>
      <p>Los programas de cualificaci&oacute;n profesional inicial 
        incluyen tres tipos de m&oacute;dulos: m&oacute;dulos espec&iacute;ficos, 
        m&oacute;dulos de formaci&oacute;n general y m&oacute;dulos conducentes 
        a la obtenci&oacute;n del t&iacute;tulo de Graduado en Educaci&oacute;n 
        Secundaria Obligatoria con una carga horaria total de 1800 horas distribuida 
        en dos cursos con un m&iacute;nimo de 900 horas cada uno.<br />
        Los dos primeros, que se realizan el primer a&ntilde;o, son obligatorios 
        para el alumnado y los terceros tienen car&aacute;cter voluntario y 
        se realizar&iacute;an el segundo a&ntilde;o.</p>
      
      <p>Los <strong>módulos obligatorios (1&ordm; curso)</strong>, 
        que habrán de ser cursados por todo el alumnado, serán los siguientes:<br>
        a) <em><strong>Módulos específicos</strong></em>, que desarrollarán las 
        competencias del perfil profesional, estarán referidos a las unidades 
        de competencia de nivel uno del Catálogo Nacional de Cualificaciones Profesionales 
        e incluirán un módulo de formación en centros de trabajo, cuyo objetivo 
        es integrar y complementar las competencias profesionales, personales 
        y sociales desarrolladas en el centro educativo y dar a conocer al alumnado 
        el mundo laboral. <br>
        Los módulos específicos correspondientes al P.C.P.I. Auxiliar Informático son:
        
  <ul> 
    <li> Montaje de sistemas y componentes informáticos (5 horas semanales). 
      </li>
    <li> Mantenimiento de sistemas y componentes informáticos (8 horas semanales):</li>
    <li> Operaciones auxiliares con tecnologías de la información y comunicación 
      (5 horas semanales). </li>
    <li> Formación en centros de trabajo (100 horas totales durante los 
      meses de mayo y junio aproximadamente, si se han superado los m&oacute;dulos 
      obligatorios restantes)</li>
  </ul>
      b)<em><strong> Módulos de formación general</strong></em>, que tendrán 
      como objetivo desarrollar las competencias básicas a través de metodologías 
      que supongan la participación activa del alumnado en los procesos de aprendizaje, 
      así como favorecer la transición desde el sistema educativo al mundo laboral. 
      <br>
      Los módulos 
      de formación general serán los siguientes: 
      <ul>
        <li>1.º Módulo de proyecto emprendedor (5 horas semanales). </li>
        <li>2.º Módulo de participación y ciudadanía (4 horas semanales). </li>
        <li>3.º Módulo de libre configuración (2 horas semanales). </li>
        </ul>
      
      <p>El alumnado tiene también una hora de tutoría lectiva semanal 
        en el 1&ordm; y el 2&ordm; curso del Programa. </p>
      <p>Los <strong>módulos voluntarios (2&ordm; curso) </strong>que 
        conducen a la obtención del título de Graduado en Educación Secundaria 
        Obligatoria, se organizan en torno a los tres ámbitos establecidos en 
        el artículo 22.7 del Decreto 231/2007, de 31 de julio, y son los siguientes: 
        <ul>
          <li> a) Módulo de comunicación (10 horas), que tiene como referente 
            el ámbito de comunicación y que incluye los aspectos básicos del currículo 
            de las materias de Lengua castellana y literatura y Primera lengua 
            extranjera de la educación secundaria obligatoria.
          <li> b) Módulo social (5 horas), que tiene como referente el ámbito 
              social y que incluye los aspectos básicos del currículo de las materias 
              de Ciencias sociales, Geografía e historia, Educación para la ciudadanía 
              y los derechos humanos y los aspectos de percepción recogidos en el 
              currículo de Educación plástica y visual y Música de la educación 
            secundaria obligatoria. </li>
          <li>c) Módulo científico-tecnológico (14 horas, de las cuales 4 se dedicar&aacute;n 
            al Proyecto T&eacute;cnico), que tiene como referente el ámbito científico- 
            tecnológico y que incluye los aspectos básicos del currículo de las 
            materias de Ciencias de la naturaleza, Matemáticas, Tecnología y los 
            aspectos relacionados con la salud y el medio natural del currículo 
            de Educación física de la educación secundaria obligatoria. </li>
          </ul>
      </p>
