<h4>Condiciones de Acceso.</h4>
<p>Para poder cursar este Ciclo formativo se necesita alguno 
  de los siguientes requisitos:</p>
<p>- T&iacute;tulo de Bachillerato LOGSE o LOE<br />
  - T&iacute;tulo de F.P.II.<br />
  - C.O.U.<br />
  - Examen de Acceso para mayores de 19 a&ntilde;os (si los cumplen en 
  el mismo a&ntilde;o). Esta prueba se realiza habitualmente durante los 
  meses de junio (inscripci&oacute;n en la segunda quincena de mayo) y 
  septiembre (inscripci&oacute;n en la segunda quincena de julio)</p>
