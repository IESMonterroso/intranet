<h4><a name="eval" id="eval"></a>Competencias Profesionales</h4>
<p>El profesional que se forma en este Ciclo tiene como competencias 
  generales:</p>
<ul>
  <li> Comercializar destinos tur&iacute;sticos, interpretando los planes 
    generales de la empresa y adapt&aacute;ndose a nuevas situaciones 
    generadas como consecuencia de los cambios producidos en las t&eacute;cnicas 
    y organizaci&oacute;n de los servicios tur&iacute;sticos.</li>
  <li>Crear productos tur&iacute;sticos de base territorial, detectando 
    ooportunidades de conservaci&oacute;n y desarrollo local y regional.</li>
  <li>Guiar grupos, organizando, desarrollando y evaluando el servicio.</li>
  <li>Gestionar servicios de informaci&oacute;n tur&iacute;stica.<br />
  </li>
</ul>
<p> Al terminar el ciclo formativo este/a profesional será capaz 
  de</p>
<ul>
  <li>Detectar oportunidades de conservaci&oacute;n y desarrollo local y 
    regional a trav&eacute;s de la creaci&oacute;n o mejora de productos 
    y servicios tur&iacute;sticos.</li>
  <li>Interpretar los planes generales de la empresa o entidad y concretarlos 
    en acciones de informaci&oacute;n, comercializaci&oacute;n y asistencia 
    de grupos.</li>
  <li>Organizar, desarrollar y evaluar el servicio de asistencia y gu&iacute;a 
    de grupos tur&iacute;sticos.</li>
  <li>Organizar, ejecutar y controlar acciones y programas de comercializaci&oacute;n 
    tur&iacute;stica.</li>
  <li>Comunicarse, al menos, en dos lenguas extranjeras.</li>
  <li>Adaptarse a nuevas situaciones generadas como consecuenca de los cambios 
    producidos en las t&eacute;cnicas y organizaci&oacute;n utilizadas 
    en el &aacute;mbito de los servicios tur&iacute;sticos.</li>
</ul>
