    <p>El IES Monterroso lleva m&aacute;s de una década formando 
    profesionales del sector turístico. Cuenta con alumnos y alumnas desde 
    los 18 años de edad, puesto que la formación Profesional es una enseñanza 
    a la que se puede acceder en cualquier momento y a cualquier edad, como 
    una forma de adquirir y completar capacidades y competencias profesionales 
    que permiten el acceso y la promoci&oacute;n laboral del alumnado.</p>
  <p>Hasta el curso 2009/2010 se ha impartido el Ciclo Formativo, de 1400 
    horas, T&eacute;cnico Superior en Informaci&oacute;n y Comercializaci&oacute;n 
    Tur&iacute;sticas, y a partir del curso 2010/2011 ese ciclo desaparece 
    y es sustituido por el Ciclo Formativo: <strong>T&eacute;cnico Superior 
    en Gu&iacute;a, Informaci&oacute;n y Asistencia Tur&iacute;sticas, </strong>de 
    2000 horas, adaptado a la L.O.E. </p>
  <p>El &aacute;mbito de actuaci&oacute;n de los T&eacute;cnicos y T&eacute;cnicas 
    Superiores en Gu&iacute;a, Informaci&oacute;n y Asistencia Tur&iacute;sticas 
    (G.I.A.T.) es el sector tur&iacute;stico, teniendo como subsectores 
    m&aacute;s cercanos la informaci&oacute;n y asistencia tur&iacute;sticas, 
    los alojamientos tur&iacute;sticos, la consultor&iacute;a tur&iacute;stica, 
    la animaci&oacute;n tur&iacute;stica, gu&iacute;a tur&iacute;stico, 
    as&iacute; como la organizaci&oacute;n de ferias y eventos.</p>
  <p>T&eacute;cnicos y T&eacute;cnicas Superiores en Gu&iacute;a, Informaci&oacute;n 
    y Asistencia Tur&iacute;sticas tienen garantizado un puesto laboral 
    gracias a la notoriedad y prestigio adquiridos a lo largo de los a&ntilde;os, 
    fruto de las relaciones establecidas con las empresas e instituciones 
    tur&iacute;sticas de la Costa del Sol (Hoteles, Agencias de Viaje, Ayuntamientos 
    y Delegaciones de Turismo, Palacios de Congresos, Puertos Deportivos, 
    ...).</p>
  <p>El alumnado tiene la posibilidad de realizar las pr&aacute;cticas de 
    empresa en alg&uacute;n pa&iacute;s de la Uni&oacute;n Europea.</p>
  <p>Desde el curso 2007/2008, el ciclo de la familia de Hosteler&iacute;a 
    y Turismo es biling&uuml;e siguiendo una andadura en pro de una formaci&oacute;n 
    profesional m&aacute;s completa y acorde a nuestros tiempos y las exigencias 
    del mercado laboral. Al ser un <strong>ciclo formativo binling&uuml;e 
    (espa&ntilde;ol-ingl&eacute;s</strong>), en algunos m&oacute;dulos profesionales 
    no ling&uuml;&iacute;sticos, la formaci&oacute;n se recibe en los dos 
    idiomas, cont&aacute;ndose adem&aacute;s con un auxiliar de conversaci&oacute;n 
    cuya lengua materna suele ser el ingl&eacute;s.</p>
  </p>
