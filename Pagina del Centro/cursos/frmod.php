<h4> Puestos de Trabajo que puede desempe&ntilde;ar. </h4>
<p>Los T&eacute;cnicos Superiores en Informaci&oacute;n 
  y Comercializaci&oacute;n Tur&iacute;sticas pueden desempe&ntilde;ar 
  los siguientes puestos de trabajo:</p>
<ul>
  <li> Gu&iacute;a local o Gu&iacute;a acompa&ntilde;ante.</li>
  <li>Informador/a tur&iacute;stico/a.</li>
  <li>Recepcionista.</li>
  <li>Animador/a tur&iacute;stico/a</li>
  <li> Promotor/a tur&iacute;stico/a.</li>
  <li>Asesor en consultor&iacute;a tur&iacute;stica.</li>
  <li>Agente de desarrollo tur&iacute;stico local.</li>
  <li>Azafata/Asistente en medios de transporte terrestre o mar&iacute;timo.</li>
  <li>Asistente en terminales (estaciones, puertos y aeropuertos).</li>
  <li>Asistente en ferias, congresos y convenciones.</li>
  <li>Encargado/a de serviciois en eventos.</li>
</ul>
