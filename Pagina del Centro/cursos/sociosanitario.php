
<div class="well well-large span10 offset1">
  <p>El Ciclo Formativo de Grado Medio Atención a Personas en Situación de Dependencia
    (título LOE), pertenece a la Familia Profesional Servicios Socioculturales
    y a la Comunidad y sustituye al Ciclo Formativo de Grado Medio Atención Sociosanitaria,
    impartido en este centro desde el curso 2007-2008. </p>
  <hr />
  <h4> <a name="caracter" id="caracter"></a>Cuando finalices los estudios serás
    capaz de: </h4>
  <p>La formaci&oacute;n se realiza en el centro educativo, a trav&eacute;s de
    una formaci&oacute;n te&oacute;rico-pr&aacute;ctica. Los contenidos se agrupan
    en los siguientes m&oacute;dulos profesionales (el equivalente a las asignaturas
    en otros estudios):</p>
  <ul>
    <li>Atender a las personas en situación de dependencia, en el ámbito domiciliario
      e institucional, a fin de mantener y mejorar su calidad de vida. </li>
    <li>Realizar actividades asistenciales, no sanitarias, psicosociales y de
      apoyo a la gestión doméstica, aplicando medidas y normas de prevención
      y seguridad y derivándolas a otros servicios cuando sea necesario. </li>
  </ul>
  <hr />
