 <hr /><h4> Requisitos de acceso:
  </h4>
  <p>Puedes acceder a un ciclo de grado medio cuando
    reúnas alguno de los siguientes requisitos:</p>
  <ul>
    <li>
      <p>Estar
        en posesión del título de Graduado en Educación Secundaria
        Obligatoria o de un nivel académico superior. </p>
    </li>
    <li>
      <p>Estar
        en posesión de un título de Técnico o de Técnico Auxiliar o equivalente
        a efectos académicos. </p>
    </li>
    <li>
      <p>Haber
        superado el segundo curso del Bachillerato Unificado y Polivalente (BUP). </p>
    </li>
  </ul>
  <ul>
    <li>
      <p class="P5">Haber
        superado la <a href="http://www.juntadeandalucia.es/educacion/formacionprofesional/index.php/pruebas-de-acceso">prueba
        de acceso a ciclos formativos de grado medio</a> (se requerirá tener,
        al menos, diecisiete años, cumplidos en el año de realización de la prueba). </p>
    </li>
  </ul>
  <hr /><h4>¿Qué vas a aprender y hacer?</h4>
  <ul>
    <li>
      <p class="P6">Realizar
        las tareas de higiene personal y vestido de las personas en situación
        de dependencia, aportando la ayuda precisa, favoreciendo al máximo su
        autonomía en las actividades de la vida diaria y manteniendo hacia ellos
        una actitud de respeto y profesionalidad. </p>
    </li>
    <li>
      <p class="P6">Organizar
        la intervención relativa a la alimentación supervisando los menús, preparando
        los alimentos y administrándolos cuando sea necesario. </p>
    </li>
    <li>
      <p class="P6">Realizar
        las actividades de mantenimiento y limpieza del domicilio garantizando
        las condiciones de habitabilidad, higiene y orden, con criterios de calidad,
        seguridad y cuidado del medio ambiente y, en su caso, tramitando la documentación
        pertinente. </p>
    </li>
    <li>
      <p class="P6">Realizar
        las intervenciones relacionadas con el estado físico de las personas
        en situación de dependencia siguiendo las pautas establecidas y mostrando
        en todo momento respeto por su intimidad. </p>
    </li>
    <li>
      <p class="P6">Realizar
        los traslados, movilizaciones y apoyo a la deambulación de las personas
        en situación de dependencia empleando los protocolos y las ayudas técnicas
        necesarias y adoptando medidas de prevención y seguridad. </p>
    </li>
    <li>
      <p class="P6">Dar
        respuesta a situaciones de emergencia y riesgo para la salud en el desarrollo
        de su actividad profesional aplicando técnicas de primeros auxilios. </p>
    </li>
    <li>
      <p class="P6">Implementar
        intervenciones de apoyo psicosocial empleando ayudas técnicas, apoyos
        de comunicación y tecnologías de la información y la comunicación. </p>
    </li>
    <li>
      <p class="P6">Aplicar
        técnicas y estrategias para el mantenimiento y desarrollo de las habilidades
        de autonomía personal y social de las personas en situación de dependencia
        empleando ayudas técnicas y de comunicación. </p>
    </li>
    <li>
      <p class="P6">Realizar
        tareas de acompañamiento y asistencia personal respetando las directrices
        del Plan Individual de Vida Independiente y las decisiones de la persona
        usuaria. </p>
    </li>
  </ul>
  <hr /><h4>Al finalizar los estudios, ¿qué podrás hacer?</h4>
  <p>Trabajar como:</p>
  <ul>
    <li>
      <p class="P7">Cuidador
        o cuidadora de personas en situación de dependencia en diferentes instituciones
        y / o domicilios. </p>
    </li>
    <li>
      <p class="P7">Gobernante
        y subgobernante de personas en situación de dependencia en instituciones. </p>
    </li>
    <li>
      <p class="P7">Auxiliar
        responsable de planta de residencias de mayores y personas con discapacidad. </p>
    </li>
    <li>
      <p class="P7">Auxiliar
        de ayuda a domicilio. </p>
    </li>
    <li>
      <p class="P7">Asistente
        de atención domiciliaria. </p>
    </li>
    <li>
      <p class="P7">Trabajador
        o trabajadora familiar. </p>
    </li>
    <li>
      <p class="P7">Auxiliar
        de educación especial. </p>
    </li>
    <li>
      <p class="P7">Asistente
        personal. </p>
    </li>
    <li>
      <p class="P7">Teleoperador/a
        de teleasistencia. </p>
    </li>
  </ul>
  <p>Seguir estudiando:</p>
  <ul>
    <li>
      <p class="P8">La
        preparación para realizar la prueba de acceso a ciclos de grado superior. </p>
    </li>
    <li>
      <p class="P8">Otro
        ciclo de Formación Profesional de Grado Medio con la posibilidad de establecer
        convalidaciones de módulos profesionales de acuerdo a la normativa vigente. </p>
    </li>
    <li>
      <p class="P8">El
        Bachillerato en cualquiera de sus modalidades. </p>
    </li>
  </ul>
  <hr /><h4>Proyectos y Actividades.</h4>
  <p class="P10">Recibirás una formación teórico-práctica en el centro educativo,
    con proyectos y actividades que te acercarán a tu futuro entorno profesional: </p>
  <ul>
    <li>
      <p class="P16">Visitas
        a recursos de atención sociosanitaria: residencias de mayores, centros
        de atención a personas con discapacidad, centros ocupacionales, centros
        de día? </p>
    </li>
    <li>
      <p class="P11">Realización
        de Jornadas de Discapacidad. </p>
    </li>
    <li>
      <p class="P11">Participación
        en Jornadas Interciclos y Jornadas de Formación Profesional. </p>
    </li>
    <li>
      <p class="P33">Charlas
        de especialistas sobre temas propios de su perfil profesional, por ejemplo
        sobre primeros auxilios? </p>
    </li>
  </ul>
  <hr /><h4>Formación en Centros de Trabajo.</h4>
  <p>Finalizarás el ciclo con el módulo de Formación en Centros de
    Trabajo (FCT) en centros y servicios de atención sociosanitaria (residencias
    de mayores, centros de personas con discapacidad, etc), complemento fundamental
    de la formación adquirida en el centro educativo. Durante este periodo de
    prácticas conocerás la realidad profesional y tendrás la oportunidad de aplicar
    todo lo aprendido. La FCT te abre las puertas del mercado laboral.</p>
